// SUMA
let a = 3, b = 2;
let z = a + b;
console.log("Resultado de la suma: " + z);

// RESTA
z = a - b;
console.log("Resultado de la resta: " + z);

// MULTIPLICACION
z = a * b;
console.log("Resultado de la multiplicacion: " + z);

// DIVISION
z = a / b;
console.log("Resultado de la division: " + z);

// MODULO (residuo de la division)
z = a % b;
console.log("Resultado de operacion modulo (residuo): " + z);

// EXPONENTE
z = a ** b;
console.log("Resultado de operador exponente: " + z);


// INCREMENTO
// PRE-INCREMENTO (el operador ++ ANTES de la variable)
z = ++a
console.log("Pre-Incremento");
console.log(a);
console.log(z);

// POST-INCREMENTO (el operador ++ DESPUES de la variable)
z = b++;
console.log("Post-Incremento");
console.log(b);
console.log(z);

// DECREMENTO
z = --a;
console.log("Decremento/Predecremento");
console.log(a);
console.log(z);

// POSTDECREMENTO
z = b--;
console.log("Postdecremento");
console.log(b);
console.log(z);